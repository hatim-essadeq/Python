# declare a tuple
example = ()
print(example)

# initialize the tuple with any value
values = (12, "sdf", True)
print(values)

# we cannot modify/add/remove values of a tuple
coordinates = (12, 44)
# coordinates[0] = 1
print(coordinates)

# get value by its index
print(values[1])

# we can declare a list of tuples
list_tuples = [(1, 2, "qsd"), (True, [1, 4])]
print(list_tuples)
